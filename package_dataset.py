#!/usr/bin/env python3
"""
package_dataset.py - Phase 2 of the Soybrary -> SDXL LoRA pipeline.

Reads data/manifests/dataset.jsonl (from build_dataset.py) and produces a
transfer-ready package:

  data/package/
    shards/shard_0000.tar ...     ~2GB tar shards, each containing flat
                                  {id}.{ext} images + {id}.txt caption sidecars
    shard_manifest.json           shard list + sha256 + counts
    dataset.toml                  kohya sd-scripts DreamBooth-style config

On the Lambda instance, the shards are extracted into a single flat directory
(default: ./train_data), which is exactly what the DreamBooth-style
[[datasets.subsets]] image_dir expects: images directly in the dir, each with
a matching .txt caption file.

Usage:
  python package_dataset.py
  python package_dataset.py --shard-size-gb 2 --image-dir /home/ubuntu/train_data
"""

import argparse
import hashlib
import io
import json
import sys
import tarfile
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parent
DATA_DIR = PROJECT_ROOT / "data"
IMAGES_DIR = DATA_DIR / "images"
MANIFESTS_DIR = DATA_DIR / "manifests"
PACKAGE_DIR = DATA_DIR / "package"


def sha256_file(path, chunk=1024 * 1024):
    h = hashlib.sha256()
    with open(path, "rb") as f:
        for block in iter(lambda: f.read(chunk), b""):
            h.update(block)
    return h.hexdigest()


def load_manifest(path):
    records = []
    with open(path, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                records.append(json.loads(line))
    return records


def build_dataset_toml(image_dir, resolution, batch_size, num_repeats):
    """
    kohya sd-scripts DreamBooth-style dataset config.

    DreamBooth-style (image_dir + .txt sidecars) is deliberately used instead
    of the fine-tuning style (metadata_file). Mixing the two, or feeding raw
    scraper metadata into metadata_file, is the classic source of
    `voluptuous.error.MultipleInvalid` failures. No metadata_file key here.
    """
    return f"""# kohya sd-scripts dataset config (DreamBooth-style, .txt caption sidecars)
# Generated by package_dataset.py. Pass via: --dataset_config dataset.toml
[general]
caption_extension = ".txt"
shuffle_caption = true
keep_tokens = 1            # pin the leading variant token (chudjak/cobson/...)
enable_bucket = true
bucket_no_upscale = true

[[datasets]]
resolution = [{resolution}, {resolution}]
batch_size = {batch_size}
min_bucket_reso = 512
max_bucket_reso = 2048

  [[datasets.subsets]]
  image_dir = "{image_dir}"
  num_repeats = {num_repeats}
  cache_info = true        # cache image sizes/captions (faster after 1st epoch)
"""


def main():
    ap = argparse.ArgumentParser(description="Package dataset into tar shards + toml.")
    ap.add_argument("--manifest", type=Path, default=MANIFESTS_DIR / "dataset.jsonl")
    ap.add_argument("--out-dir", type=Path, default=PACKAGE_DIR)
    ap.add_argument("--shard-size-gb", type=float, default=2.0,
                    help="Approx uncompressed bytes per shard. Default 2 GB.")
    ap.add_argument("--image-dir", type=str, default="/home/ubuntu/train_data",
                    help="Path the dataset.toml will point image_dir at "
                         "(the extraction dir on the Lambda instance).")
    ap.add_argument("--resolution", type=int, default=1024)
    ap.add_argument("--batch-size", type=int, default=4)
    ap.add_argument("--num-repeats", type=int, default=1)
    args = ap.parse_args()

    if not args.manifest.exists():
        sys.exit(f"ERROR: manifest not found at {args.manifest}. Run build_dataset.py first.")

    records = load_manifest(args.manifest)
    if not records:
        sys.exit("ERROR: manifest is empty.")
    print(f"Loaded {len(records)} records from {args.manifest}")

    shards_dir = args.out_dir / "shards"
    shards_dir.mkdir(parents=True, exist_ok=True)

    shard_size_bytes = int(args.shard_size_gb * 1024 * 1024 * 1024)
    shard_manifest = []
    shard_idx = 0
    cur_tar = None
    cur_tar_path = None
    cur_bytes = 0
    cur_count = 0
    written = 0
    missing = 0

    def open_new_shard(idx):
        path = shards_dir / f"shard_{idx:04d}.tar"
        return tarfile.open(path, "w"), path

    def close_shard(tar, path, count):
        tar.close()
        shard_manifest.append({
            "name": path.name,
            "images": count,
            "bytes": path.stat().st_size,
            "sha256": sha256_file(path),
        })
        print(f"  wrote {path.name}: {count} images, {path.stat().st_size/1e9:.2f} GB")

    cur_tar, cur_tar_path = open_new_shard(shard_idx)

    for i, rec in enumerate(records):
        img_path = IMAGES_DIR / rec["file"]
        if not img_path.exists():
            missing += 1
            continue

        img_bytes = img_path.read_bytes()
        caption_bytes = rec["caption"].encode("utf-8")
        stem = str(rec["id"])

        # Roll to a new shard if this image would exceed the target size
        # (but always keep at least one image per shard).
        if cur_bytes and (cur_bytes + len(img_bytes)) > shard_size_bytes:
            close_shard(cur_tar, cur_tar_path, cur_count)
            shard_idx += 1
            cur_tar, cur_tar_path = open_new_shard(shard_idx)
            cur_bytes = 0
            cur_count = 0

        # image
        img_info = tarfile.TarInfo(name=f"{stem}.{rec['ext']}")
        img_info.size = len(img_bytes)
        cur_tar.addfile(img_info, io.BytesIO(img_bytes))

        # caption sidecar (same stem, .txt)
        txt_info = tarfile.TarInfo(name=f"{stem}.txt")
        txt_info.size = len(caption_bytes)
        cur_tar.addfile(txt_info, io.BytesIO(caption_bytes))

        cur_bytes += len(img_bytes) + len(caption_bytes)
        cur_count += 1
        written += 1

        if (i + 1) % 20000 == 0:
            print(f"  packaged {i+1}/{len(records)} (shard {shard_idx})", flush=True)

    if cur_count:
        close_shard(cur_tar, cur_tar_path, cur_count)
    else:
        cur_tar.close()
        cur_tar_path.unlink(missing_ok=True)

    # Write shard manifest
    total_bytes = sum(s["bytes"] for s in shard_manifest)
    manifest_out = {
        "image_dir": args.image_dir,
        "total_images": written,
        "missing_files": missing,
        "num_shards": len(shard_manifest),
        "total_bytes": total_bytes,
        "shards": shard_manifest,
    }
    (args.out_dir / "shard_manifest.json").write_text(
        json.dumps(manifest_out, indent=2), encoding="utf-8"
    )

    # Write dataset.toml
    toml = build_dataset_toml(
        args.image_dir, args.resolution, args.batch_size, args.num_repeats
    )
    (args.out_dir / "dataset.toml").write_text(toml, encoding="utf-8")

    print("\n=== PACKAGE SUMMARY ===")
    print(f"Images packaged: {written}")
    print(f"Missing files:   {missing}")
    print(f"Shards:          {len(shard_manifest)}")
    print(f"Total size:      {total_bytes/1e9:.2f} GB")
    print(f"Output dir:      {args.out_dir}")
    print(f"dataset.toml image_dir -> {args.image_dir}")


if __name__ == "__main__":
    main()
